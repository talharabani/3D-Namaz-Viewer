// Get current language from localStorage
export const getCurrentLanguage = () => {
  return localStorage.getItem('language') || 'en';
};

// Set language in localStorage
export const setLanguage = (language) => {
  localStorage.setItem('language', language);
};

// Translation function
export const t = (key) => {
  const currentLang = getCurrentLanguage();
  return translations[currentLang]?.[key] || key;
};

// Custom hook for translations
export const useTranslation = () => {
  const [currentLang, setCurrentLangState] = useState(getCurrentLanguage());
  
  const t = (key, params = {}) => {
    const translation = translations[currentLang]?.[key] || translations.en[key] || key;
    
    // Replace parameters in translation
    return translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey] || match;
    });
  };
  
  const setLanguageAndUpdate = (language) => {
    setLanguage(language);
    setCurrentLangState(language);
    
    // Update document direction for RTL languages
    if (language === 'ur') {
      document.documentElement.dir = 'rtl';
      document.documentElement.lang = 'ur';
      document.body.classList.add('rtl');
      document.body.classList.remove('ltr');
    } else {
      document.documentElement.dir = 'ltr';
      document.documentElement.lang = 'en';
      document.body.classList.add('ltr');
      document.body.classList.remove('rtl');
    }
  };
  
  return { t, currentLang, setLanguage: setLanguageAndUpdate };
};
